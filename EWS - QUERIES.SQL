CREATE TABLE Transacting_Range AS
WITH FirstNonNullDate AS (
  SELECT 
    g.CIF_ID, 
    MAX(l.NEXT_DMD_DATE) AS MaxNextDmdDate,
    ROW_NUMBER() OVER (PARTITION BY g.CIF_ID ORDER BY MAX(l.NEXT_DMD_DATE)) AS rn
  FROM 
    tbaadm.lrs@finacle l
    LEFT JOIN TBAADM.GAM@FINACLE g ON l.ACID = g.ACID
  WHERE 
    g.CIF_ID IN (SELECT CIF_ID FROM CS_DETAIL_RETAIL)
    AND g.SCHM_CODE LIKE 'L%' 
    AND l.NEXT_DMD_DATE IS NOT NULL
  GROUP BY 
    g.CIF_ID
)

SELECT 
  cif_id, 
  MaxNextDmdDate - INTERVAL '28' DAY AS StartDate,
  MaxNextDmdDate - INTERVAL '7' DAY AS EndDate
FROM 
  FirstNonNullDate
WHERE 
  rn = 1;

WITH MAINCTE AS (
  SELECT D.CIF_ID, G.ACID, DISB_DATE 
  FROM CS_DETAIL_RETAIL D
  LEFT JOIN TBAADM.GAM@FINACLE G ON G.CIF_ID = D.CIF_ID
  WHERE G.SCHM_CODE NOT LIKE 'L%'
),
RATECTE AS (
  SELECT
      FXD_CRNCY_CODE,
      RTLIST_DATE,
      VAR_CRNCY_UNITS
  FROM
      TBAADM.RTH@FINACLE
  WHERE
      VAR_CRNCY_CODE = 'KES'
      AND RTLIST_NUM = 1
      AND RATECODE = 'MID'
),
TRANSCTE AS ( 
  SELECT 
    H.ACID, 
    H.TRAN_ID, 
    H.PART_TRAN_TYPE, 
    H.TRAN_CRNCY_CODE, 
    H.TRAN_DATE,
    CASE
      WHEN H.TRAN_CRNCY_CODE <> 'KES' THEN H.TRAN_AMT * COALESCE(
        (
            SELECT R.VAR_CRNCY_UNITS
            FROM RATECTE R
            WHERE R.FXD_CRNCY_CODE = H.TRAN_CRNCY_CODE
              AND R.RTLIST_DATE <= H.TRAN_DATE
            ORDER BY R.RTLIST_DATE DESC
            FETCH FIRST 1 ROWS ONLY
        ),
        (
            SELECT R.VAR_CRNCY_UNITS
            FROM RATECTE R
            WHERE R.FXD_CRNCY_CODE = H.TRAN_CRNCY_CODE
            ORDER BY R.RTLIST_DATE DESC
            FETCH FIRST 1 ROWS ONLY
        )
      )
      ELSE H.TRAN_AMT
    END AS CONVERTED_AMOUNT
  FROM TBAADM.HTD@FINACLE H
  LEFT JOIN TBAADM.GAM@FINACLE G ON H.ACID = G.ACID
  LEFT JOIN TRANSACTING_RANGE T ON G.CIF_ID = T.CIF_ID
  WHERE H.TRAN_PARTICULAR NOT LIKE '%REVERSAL%' 
    AND H.TRAN_PARTICULAR NOT LIKE 'REV%'
    AND G.SCHM_CODE NOT LIKE '%CAL29%'
      AND H.TRAN_PARTICULAR NOT LIKE 'REV%'
      AND H.TRAN_DATE BETWEEN T.StartDate AND T.EndDate
)

SELECT 
  MAINCTE.CIF_ID, 
  TRANSCTE.ACID, 
  TRANSCTE.TRAN_ID, 
  TRANSCTE.PART_TRAN_TYPE, 
  TRANSCTE.TRAN_CRNCY_CODE, 
  TRANSCTE.TRAN_DATE, 
  TRANSCTE.CONVERTED_AMOUNT
FROM MAINCTE
LEFT JOIN TRANSCTE ON TRANSCTE.ACID = MAINCTE.ACID;


CREATE TABLE CS_TRANS_RETAIL AS
(
SELECT * FROM 
(
WITH MAINCTE AS
(
SELECT D.CIF_ID, G.ACID, DISB_DATE, DISB_DATE_3WK FROM CS_DETAIL_RETAIL D
LEFT JOIN TBAADM.GAM@FINACLE G ON G.CIF_ID = D.CIF_ID
WHERE G.SCHM_CODE NOT LIKE 'L%' 
),
TRANSCTE AS
( 
SELECT H.ACID, H.TRAN_ID, H.PART_TRAN_TYPE, H.TRAN_CRNCY_CODE, H.TRAN_DATE, H.TRAN_AMT ORIGINAL_AMOUNT, 
CASE WHEN H.TRAN_CRNCY_CODE <> 'KES' THEN H.TRAN_AMT * VAR_CRNCY_UNITS ELSE H.TRAN_AMT END CONVERTED_AMOUNT
FROM TBAADM.HTD@FINACLE H
LEFT JOIN TBAADM.RTH@FINACLE RTH ON RTH.FXD_CRNCY_CODE = H.TRAN_CRNCY_CODE AND H.TRAN_DATE = RTH.RTLIST_DATE AND RTH.VAR_CRNCY_CODE = 'KES' AND RTH.RTLIST_NUM = 1 AND RTH.RATECODE = 'MID'
WHERE H.TRAN_PARTICULAR NOT LIKE '%REVERSAL%' 
AND H.TRAN_PARTICULAR NOT LIKE 'REV%'
AND TRAN_DATE BETWEEN '05-JAN-22' AND '21-JUL-23'
)
SELECT MAINCTE.CIF_ID, TRANSCTE.ACID, TRANSCTE.TRAN_ID, TRANSCTE.PART_TRAN_TYPE, TRANSCTE.TRAN_CRNCY_CODE, TRANSCTE.TRAN_DATE, TRANSCTE.ORIGINAL_AMOUNT,  CONVERTED_AMOUNT
FROM MAINCTE
LEFT JOIN TRANSCTE ON TRANSCTE.ACID = MAINCTE.ACID
)
);

