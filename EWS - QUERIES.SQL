CREATE TABLE Base_table_ews AS
(
SELECT
    C.CIF_ID,
    G.ACID,
    FLOOR((SYSDATE -C.DATE_OF_BIRTH)/ 365) CUST_AGE,
    FLOOR((SYSDATE - C.CUST_OPN_DATE)/ 365) AGE_WITH_BANK,
    C.SEGMENTATION_CLASS,
    M.CUSTOMERINCOME,
    M.SECTOR,
    M.SUBSECTOR,
    S.SECURED_FLG,
    M.PHYSICALADDRESS,
    M.MARITALSTATUS,
    M.OTG_TRANSACTING,
    G.SCHM_CODE,
    S.SCHM_DESC,
    L.DIS_AMT,
    LHT.APPLICABLE_DATE + 21 DISB_DATE_3WK,
    LHT.APPLICABLE_DATE DISB_DATE,
    L.REP_PERD_MTHS,
    GAC.DPD_CNTR,
    LRS.FLOW_ID,
    SUM(LRS.FLOW_AMT)FLOW_AMT,
    COUNT(LRS.FLOW_AMT) NO_OF_INSTALLMENTS
FROM
    TBAADM.CMG@FINACLE C,
    TBAADM.GAM@FINACLE G,
    EDW.MDM_IMKE M,
    TBAADM.GSP@FINACLE S,
    TBAADM.LAM@FINACLE L,
    TBAADM.GAC@FINACLE GAC,
    TBAADM.LRS@FINACLE LRS,
    TBAADM.LHT@FINACLE
WHERE
    C.CIF_ID = G.CIF_ID
    AND C.CIF_ID = M.CUSTOMERID
    AND G.SCHM_CODE = S.SCHM_CODE
    AND L.ACID = G.ACID
    AND GAC.ACID = G.ACID
    AND LRS.ACID = G.ACID
    AND LHT.ACID = G.ACID
    AND C.SEGMENTATION_CLASS IN ('BL03', 'BL05')
    AND G.SCHM_CODE IN ('LAL02', 'LAL03', 'LAL27', 'LAF06', 'LAL06', 'LAL30', 'LAL10', 'LAL32', 'LAL21', 'LAF01')
    AND G.ACCT_CLS_DATE IS NULL
    AND CLR_BAL_AMT < 0
    AND FLOW_AMT > 0
GROUP BY
    C.CIF_ID,
    G.ACID,
    C.DATE_OF_BIRTH,
    C.CUST_OPN_DATE,
    C.SEGMENTATION_CLASS,
    M.CUSTOMERINCOME,
    M.SECTOR,
    M.SUBSECTOR,
    S.SECURED_FLG,
    M.PHYSICALADDRESS,
    M.MARITALSTATUS,
    M.OTG_TRANSACTING,
    G.SCHM_CODE,
    S.SCHM_DESC,
    L.DIS_AMT,
    LHT.APPLICABLE_DATE,
    L.REP_PERD_MTHS,
    GAC.DPD_CNTR,
    LRS.FLOW_ID
);

